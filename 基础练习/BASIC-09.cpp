/**
 * 基础练习 特殊回文数
 */

#include <iostream>

using namespace std;

int main()
{
    int n, x0, x1, x2, x3, x4, x5;

    cin >> n;

    for (int i = 10000; i <= 99999; ++i)
    {
        x4 = i / 10000;

        x3 = (i - (x4 * 10000)) / 1000;

        x2 = (i - (x4 * 10000) - (x3 * 1000)) / 100;

        x1 = (i - (x4 * 10000) - (x3 * 1000) - (x2 * 100)) / 10;

        x0 = (i - (x4 * 10000) - (x3 * 1000) - (x2 * 100) - (x1 * 10));

        if (x0 == x4 && x1 == x3 && x0 + x1 + x2 + x3 + x4 == n)
            cout << i << endl;
    }

    for (int i = 100000; i <= 999999; ++i)
    {
        x5 = i / 100000;

        x4 = (i - (x5 * 100000)) / 10000;

        x3 = (i - (x5 * 100000) - (x4 * 10000)) / 1000;

        x2 = (i - (x5 * 100000) - (x4 * 10000) - (x3 * 1000)) / 100;

        x1 = (i - (x5 * 100000) - (x4 * 10000) - (x3 * 1000) - (x2 * 100)) / 10;

        x0 = (i - (x5 * 100000) - (x4 * 10000) - (x3 * 1000) - (x2 * 100) - (x1 * 10));

        if (x0 == x5 && x1 == x4 && x2 == x3 && x0 + x1 + x2 + x3 + x4 + x5 == n)
            cout << i << endl;
    }

    return 0;
}
